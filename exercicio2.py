pergunta = int(input("Digite um numero positivo de '1' a '5': " ))

if pergunta == 0:
    print("Zero")

elif pergunta == 1:
    print("Um")

elif pergunta == 2:
    print("Dois")

elif pergunta == 3:
    print("Tres")

elif pergunta == 4:
    print("Quatro")

elif pergunta == 5:
    print("Cinco")

else:
    print("Digite um numero valido")
